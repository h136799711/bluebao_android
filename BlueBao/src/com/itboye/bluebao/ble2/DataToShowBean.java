package com.itboye.bluebao.ble2;

public class DataToShowBean {

	private String time;//时间
	private String speed;//速度
	private String miles;//距离值
	private String cars;//卡路里
	private String totalMiles;//总距离
	private String xinlv;//心率
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getMiles() {
		return miles;
	}
	public void setMiles(String miles) {
		this.miles = miles;
	}
	public String getCars() {
		return cars;
	}
	public void setCars(String cars) {
		this.cars = cars;
	}
	public String getTotalMiles() {
		return totalMiles;
	}
	public void setTotalMiles(String totalMiles) {
		this.totalMiles = totalMiles;
	}
	public String getXinlv() {
		return xinlv;
	}
	public void setXinlv(String xinlv) {
		this.xinlv = xinlv;
	}
}
